def using_include(array, element)
  array.include?(element) #return true if element is included in array
end

def using_sort(array)
  array.sort #returns the sorted array without modification
end

def using_reverse(array)
  array = array.reverse #returns a reversed version of the array
end

def using_first(array)
  array.first
end

def using_last(array)
  array.last
end

def using_size(array)
  array.size
end
